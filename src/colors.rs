#![allow(dead_code)]
use sdl2::pixels::Color;

pub fn white() -> Color {
    Color::RGB(255, 255, 255)
}

pub fn black() -> Color {
    Color::RGB(0, 0, 0)
}

pub fn grey() -> Color {
    Color::RGB(150, 150, 150)
}

pub fn blue() -> Color {
    Color::RGB(255, 0, 0)
}

pub fn green() -> Color {
    Color::RGB(0, 255, 0)
}

pub fn red() -> Color {
    Color::RGB(255, 0, 0)
}
